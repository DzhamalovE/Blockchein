def simple_hash(data):
    """
    Қарапайым хэш алгоритмінің іске асырылуы.
    Жол түріндегі енгізуді қабылдап, хэшталған мәнді қайтарады.
    """
    hash_value = 0
    prime = 1  # Араластыру үшін кішкентай жай сан

    # Енгізу мәліметтерін символдар тізіміне түрлендіру
    for char in data:
        # Символдың ASCII мәнін қолданып хэш мәнін жаңарту
        hash_value = (hash_value * prime + ord(char)) % (2**32)  # 32 биттің ішінде сақтау

    return hash_value

# Хэш функциясын тексеру
мәлімет = "14"
хэш_нәтиже = simple_hash(мәлімет)

print(f"Енгізу мәліметтері: {мәлімет}")
print(f"Хэш нәтижесі: {хэш_нәтиже}")
